package main

import "fmt"

func main() {

	a := 10                    // int
	b := "golang"              // string
	c := 4.17                  // float64
	d := true                  // bool
	e := "Hello"               // string
	f := `Do you like my hat?` // string
	g := 'M'                   // rune

	fmt.Printf("%v \n", a)
	fmt.Printf("%v \n", b)
	fmt.Printf("%v \n", c)
	fmt.Printf("%v \n", d)
	fmt.Printf("%v \n", e)
	fmt.Printf("%v \n", f)
	fmt.Printf("%v \n", g)
}
